# Write a program that will convert celsius value to fahrenheit
c1=int(input("Enter First Temperature in Celsius: "))
c2=int(input("Enter Second Temperature in Celsius: "))

f1 = ( c1 * 9/5 ) + 32
f2 = ( c2 * 9/5 ) + 32

print("First Temperature in Fahrenheit: ",f1)
print("Second Temperature in Fahrenheit: ",f2)